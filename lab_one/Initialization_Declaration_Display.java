public class Initialization_Declaration_Display {
    public static void main(String[] args)
    {
        // Declaration of variables
        /* 1) Write a program to Declare,Initialize and Display the data of all the primitive
       datatypes of java. */
    
        // Declaration of variables
        byte myByteVar;
        short myShortVar;
        int myIntVar;
        long myLongVar;
        float myFloatVar;
        double myDoubleVar;
        char myCharVar;
        boolean myBooleanVar;

        // Initializing values
        myByteVar = 101;                   // Initializing byte variable
        myShortVar = 12345;                // Initializing short variable
        myIntVar = 222211111;              // Initializing int variable
        myLongVar = 585502225158888651L;  // Initializing long variable
        myFloatVar = 444.44f;              // Initializing float variable
        myDoubleVar = 2211.2255;           // Initializing double variable
        myCharVar = 'B';                   // Initializing char variable
        myBooleanVar = true;               // Initializing boolean variable

        // Displaying the values
        System.out.println("myByteVar: " + myByteVar);
        System.out.println("myShortVar: " + myShortVar);
        System.out.println("myIntVar: " + myIntVar);
        System.out.println("myLongVar: " + myLongVar);
        System.out.println("myFloatVar: " + myFloatVar);
        System.out.println("myDoubleVar: " + myDoubleVar);
        System.out.println("myCharVar: " + myCharVar);
        System.out.println("myBooleanVar: " + myBooleanVar);
    }
}

    
    

